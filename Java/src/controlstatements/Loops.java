package controlstatements;

public class Loops {
	public static void main(String[] args)
	{
		int j[]=new int[3];
		j[0]=100;
		j[1]=200;
		j[2]=300;
		for(int x:j)
		{
			
			System.out.println(x);
		}
		
		
		
		for(int i=0;i<=10;i++)
		{
			System.out.println(i);
		}
		
		
		int a=0;
		while(a<=10)
		{
			
			System.out.println(a);
			a++;
		}
	}

}
